package main.template.data;

public class SectionTemplateData implements TemplateData {

	String name;
	String images;


	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public String getImages() {
		return images;
	}

	public void setImages(String images) {
		this.images = images;
	}
	
}
